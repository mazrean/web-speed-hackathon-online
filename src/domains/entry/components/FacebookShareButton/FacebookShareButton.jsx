import React, { useEffect } from 'react';

const FACEBOOK_SDK =
  'https://connect.facebook.net/en_US/sdk.js#xfbml=1&version=v3.0';

export function FacebookShareButton() {
  useEffect(() => {
    if ('FB' in globalThis) {
      globalThis.FB.XFBML.parse();
      return;
    }

    const b = getElementByTagName("body");
    const ele = b.items(0);

    const new_ele = document.createElement("script");
    new_ele.setAttribute("crossorigin", "anonymous")
    new_ele.setAttribute("src", FACEBOOK_SDK)
    ele.appendChild(new_ele)

    return () => {
      new_ele.remove();
    };
  }, []);

  return (
    <div className="entry-FacebookShareButton">
      <div id="fb-root" />
      <div
        className="fb-like"
        data-href={globalThis.location.href}
        data-width=""
        data-layout="button_count"
        data-action="like"
        data-size="small"
        data-share="false"
      ></div>
    </div>
  );
}
